var buttonClick = document.getElementById('btn-click');
var bgdiv = document.querySelector('main div');


function bgcolor() {
    buttonClick.style.backgroundColor = "rgb(" + Math.floor(Math.random() * 255) + "," + Math.floor(Math.random() * 255) + "," + Math.floor(Math.random() * 255) + ")"
    bgdiv.style.backgroundColor = "rgb(" + Math.floor(Math.random() * 255) + "," + Math.floor(Math.random() * 255) + "," + Math.floor(Math.random() * 255) + ")"
}



buttonClick.onclick = bgcolor;